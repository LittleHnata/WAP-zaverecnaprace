BOBŘÍK INFORMATIKY je předmětová soutěž, podporovaná Ministerstvem školství
Soutěž je cílena na běžného bystrého žáka se zájmem o svět technologií.
Klade si za cíl seznamovat žáky a jejich učitele s tím, co to jsou informatické otázky a problémy, a že informatika není 

totéž co ovládání počítače.
Soutěží se v pěti věkových kategoriích od 4. ročníku ZŠ po maturitu.
Soutěžící odpovídají na otázky z oblasti algoritmizace, porozumění informacím, řešení problémů a digitální gramotnosti.
Soutěžící vyplňují online test, v němž vybírají z několika odpovědí nebo přemísťují objekty na obrazovce.
Složením testu (více krátkých otázek) soutěž připomíná Matematického klokana, probíhá ovšem na počítačích, v počítačové
učebně.
